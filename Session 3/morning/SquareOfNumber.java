public class SquareOfNumber {

    static int numSqr(int x){
        return x*x;
    }

    public static void main(String[] args) {
        System.out.println(numSqr(5));
        System.out.println(numSqr(1));
        System.out.println(numSqr(-4));
        System.out.println(numSqr(10));
    }

    
}